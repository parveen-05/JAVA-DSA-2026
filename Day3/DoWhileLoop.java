public class DoWhileLoop {
    public static void main(String args[]){
        int i = 0;
        do{
            System.out.println("HI");
            i=i+1;
        } while(i<5);

        int j=12;
        do{
            System.out.println("Parveen");
        } while(j<3);
    }
}
