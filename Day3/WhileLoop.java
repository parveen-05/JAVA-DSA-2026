public class WhileLoop {
    public static void main(String args[]){
        int i=0;
        while(i<11){
            System.out.println("Parveen");
            i=i+1;
        }
        int j=0;
        while(j<6){
            System.out.println(j);
            j=j+1;
        }
    }
}
